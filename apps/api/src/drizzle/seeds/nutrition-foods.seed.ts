async function main() {
  for (const food of foods) {
    await db
      .insert(nutritionFoods)
      .values(food)
      .onConflictDoUpdate({
        target: [nutritionFoods.nameVi, nutritionFoods.state],
        set: {
          nameEn: food.nameEn,
          aliases: food.aliases,
          category: food.category,
          calories100g: food.calories100g,
          protein100g: food.protein100g,
          carbs100g: food.carbs100g,
          fat100g: food.fat100g,
          fiber100g: food.fiber100g ?? null,
          sugar100g: food.sugar100g ?? null,
          sodium100g: food.sodium100g ?? null,
          updatedAt: new Date(),
        },
      });
  }

  console.log(`Seeded ${foods.length} nutrition foods.`);
  process.exit(0);
}

main().catch((error) => {
  console.error('Nutrition food seed failed:', error);
  process.exit(1);
});
